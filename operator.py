# +	Addition	x + y	
# -	Subtraction	x - y	
# *	Multiplication	x * y	
# /	Division	x / y	
# %	Modulus	x % y	
# **	Exponentiation	x ** y	
# //	Floor division	x // y
x = 5
y = 3

print(x + y) #8
print(x - y) #2
print(x * y) #15
print(x / y) #1.6666666666666667 : full quot
print(x % y) #2 # modular
print(x ** y) #same as 2*2*2*2*2 #  125
print(x // y) # 1 : division  :integer quot

#the floor division // rounds the result down to the nearest whole number

